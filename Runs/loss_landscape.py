device = "cuda" if torch.cuda.is_available() else "cpu"
model_eval = model.to(device).eval()

# ------------------------------------------------------------
# 1) Tokenize prefix + target
# ------------------------------------------------------------
full_text = prefix_text + target_text

enc = tokenizer(
    full_text,
    truncation=True,
    max_length=256,
    padding=False,
    return_tensors="pt",
    add_special_tokens=False,
)

input_ids = enc["input_ids"].to(device)        # (1, T)
attention_mask = enc["attention_mask"].to(device)

prefix_len = len(tokenizer(prefix_text, add_special_tokens=False)["input_ids"])
T = input_ids.size(1)

labels = input_ids.clone()
labels[:, :prefix_len] = -100   # loss only on target tokens

print("Device:", device)
print("Sequence length:", T)
print("Prefix length:", prefix_len)

# ------------------------------------------------------------
# 2) Input embeddings
# ------------------------------------------------------------
with torch.no_grad():
    embed_layer = model_eval.get_input_embeddings()
    embeds0 = embed_layer(input_ids)            # (1, T, D)

target_start = prefix_len
target_end = T
V = embeds0[:, target_start:target_end, :].clone()

# ------------------------------------------------------------
# 3) Orthogonal directions in target-embedding space
# ------------------------------------------------------------
def orthogonal_directions_like(V_slice, seed=0):
    torch.manual_seed(seed)
    x = torch.randn_like(V_slice)

    torch.manual_seed(seed + 1)
    y = torch.randn_like(V_slice)

    x_flat = x.reshape(-1)
    y_flat = y.reshape(-1)

    # Gram–Schmidt
    y_flat -= (torch.dot(y_flat, x_flat) /
               torch.dot(x_flat, x_flat).clamp_min(1e-12)) * x_flat

    # Normalize
    x_flat /= torch.norm(x_flat).clamp_min(1e-12)
    y_flat /= torch.norm(y_flat).clamp_min(1e-12)

    x = x_flat.reshape_as(V_slice)
    y = y_flat.reshape_as(V_slice)

    # scale to ||V||
    Vn = torch.norm(V_slice.reshape(-1)).clamp_min(1e-12)
    return x * Vn, y * Vn

x_dir, y_dir = orthogonal_directions_like(V, seed=0)

# ------------------------------------------------------------
# 4) Target-only loss from perturbed embeddings
# ------------------------------------------------------------
@torch.no_grad()
def target_only_loss_from_embeds(inputs_embeds):
    out = model_eval(
        inputs_embeds=inputs_embeds,
        attention_mask=attention_mask,
    )
    logits = out.logits

    shift_logits = logits[:, :-1, :].contiguous()
    shift_labels = labels[:, 1:].contiguous()

    loss = F.cross_entropy(
        shift_logits.view(-1, shift_logits.size(-1)),
        shift_labels.view(-1),
        ignore_index=-100,
        reduction="mean",
    )
    return float(loss.item())

base_loss = target_only_loss_from_embeds(embeds0)
print("Base target loss (center):", base_loss)

# ------------------------------------------------------------
# 5) Embedding-space landscape (centered)
# ------------------------------------------------------------
def embedding_landscape(steps=31, distance=0.02):
    coords = torch.linspace(-distance, distance, steps, device=device)
    Z = torch.empty((steps, steps), dtype=torch.float64)

    for i, a in enumerate(coords):
        for j, b in enumerate(coords):
            embeds = embeds0.clone()
            embeds[:, target_start:target_end, :] = V + a * x_dir + b * y_dir
            Z[i, j] = target_only_loss_from_embeds(embeds)

    return coords.cpu().numpy(), Z.numpy()

steps = 31
coords, Z = embedding_landscape(steps=steps, distance=30)

Dloss = Z - base_loss
center_idx = steps // 2

# ------------------------------------------------------------
# 6) Print lowest loss on the surface
# ------------------------------------------------------------
min_idx = np.unravel_index(np.argmin(Z), Z.shape)
min_loss = float(Z[min_idx])
